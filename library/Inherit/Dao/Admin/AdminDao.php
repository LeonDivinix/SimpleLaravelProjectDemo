namespace Library\Inherit\Dao\Admin;


use Library\Inherit\Dao\BaseDao;

abstract class AdminDao extends BaseDao
{
    /**
     * 构建表名
     * @return string
     */
    abstract function buildTableName();

    /**
     * 构造函数
     * @param string $dbConfig 数据库连接配置key
     */
    public function __construct($dbConfig = "")
    {
        if (empty($dbConfig)) {
            $dbConfig = "admin";
        }
        parent::__construct($dbConfig); // TODO: Change the autogenerated stub
        $this->setTable($this->buildTableName());
    }


}